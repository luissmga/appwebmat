//operadores matemáticos
// + - * /  **
// += -= *= /= %=
console.log(10 + 10);
console.log(10 - 10);
console.log(10 / 10);
console.log(10 * 10);
console.log(10 % 10);
console.log(10 ** 10); //potencia

let num = 15;
console.log(num += 5); //num = num + 5
console.log(num -= 5);
console.log(num *= 5);
console.log(num /= 5);
console.log(num %= 5);

//operadores booleanos
//> < >= <= != ===
console.log(1 > 2);
console.log(1 < 2);
console.log(1 >= 2);
console.log(1 <= 2);
console.log(1 !== 2);
console.log(1 === 2);


//operadores lógicos
//and &&
console.log(true && true);
//or ||
console.log(true || false);
//not !
console.log(!true);